class EventSerializer(serializers.ModelSerializer):
    image = serializers.ImageField(use_url=True, required=False, allow_null=True)
    host_company = serializers.SerializerMethodField()

    class Meta:
        model = Event
        fields = '__all__'

    def get_host_company(self, obj):
        creator = getattr(obj, "created_by", None)
        if creator is None:
            return ""

        full_name = creator.get_full_name().strip()
        if full_name:
            return full_name

        if creator.first_name:
            return creator.first_name

        if creator.username:
            return creator.username

        return creator.email or ""


class ListingSerializer(serializers.ModelSerializer):
    image = serializers.ImageField(use_url=True, required=False, allow_null=True)
    location = serializers.SerializerMethodField()

    class Meta:
        model = Listing
        fields = '__all__'

    def get_location(self, obj):
        return obj.city

    def to_internal_value(self, data):
        mutable_data = data.copy()

        if mutable_data.get('location') and not mutable_data.get('city'):
            mutable_data['city'] = mutable_data['location']

        return super().to_internal_value(mutable_data)


